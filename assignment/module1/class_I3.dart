class Marks1
{
  var mark;

  void grades()
  {
    if(mark>=91){
      print('AA');
    }
    else if(mark>=81){
      print('AB');
    }
    else if(mark>=71){
      print('BB');
    }
    else if(mark>=61){
      print('BC');
    }
    else if(mark>=51){
      print('CC');
    }
    else if(mark>=41){
      print('CD');
    }
    else if(mark>=40){
      print('DD');
    }
    else{
      print('fail');
    }
  }
}